<?php

namespace App\Helpers;

class Helper
{
    public static $STATUS_ON = 1;
    public static $STATUS_OFF = 0;
    public static $IS_IP_CHECKED = 'is_ip_checked';
    public static $IS_IP_FB = 'is_ip_fb';
    public static $IP_DATA_REGION = 'ip_region';
    public static $USER_AGENT_DATA = 'user_agent';

    public static $LOGIN_WRONG_PASSWD = 1;
    public static $LOGIN_NEED_2FA = 2;
    public static $LOGIN_CHECKPOINT = 3;
    public static $LOGIN_SUCCESS = 4;

    public static function clearXSS($string)
    {
        $string = nl2br($string);
        $string = trim(strip_tags($string));
        $string = self::removeScripts($string);

        return $string;
    }

    public static function removeScripts($str)
    {
        $regex =
            '/(<link[^>]+rel="[^"]*stylesheet"[^>]*>)|' .
            '<script[^>]*>.*?<\/script>|' .
            '<style[^>]*>.*?<\/style>|' .
            '<!--.*?-->/is';

        return preg_replace($regex, '', $str);
    }

    public static function getCookieFBFilePath($username) {
        $username = PhoneHelper::convert($username);
        $username = "cookies/fb/".urlencode($username).".txt";
        return $username;
    }

    public static function bindParams($url, array $variables)
    {
        return preg_replace_callback('#{(.*?)}#',
            function($match) use ($variables){
                $match[1] = trim($match[1], '$');
                return $variables[$match[1]];
            }, $url);
    }

    public static function convertVIToEN($str) {
        $str = preg_replace("/(à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ)/", "a", $str);
        $str = preg_replace("/(è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ)/", "e", $str);
        $str = preg_replace("/(ì|í|ị|ỉ|ĩ)/", "i", $str);
        $str = preg_replace("/(ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ)/", "o", $str);
        $str = preg_replace("/(ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ)/", "u", $str);
        $str = preg_replace("/(ỳ|ý|ỵ|ỷ|ỹ)/", "y", $str);
        $str = preg_replace("/(đ)/", "d", $str);
        $str = preg_replace("/(À|Á|Ạ|Ả|Ã|Â|Ầ|Ấ|Ậ|Ẩ|Ẫ|Ă|Ằ|Ắ|Ặ|Ẳ|Ẵ)/", "A", $str);
        $str = preg_replace("/(È|É|Ẹ|Ẻ|Ẽ|Ê|Ề|Ế|Ệ|Ể|Ễ)/", "E", $str);
        $str = preg_replace("/(Ì|Í|Ị|Ỉ|Ĩ)/", "I", $str);
        $str = preg_replace("/(Ò|Ó|Ọ|Ỏ|Õ|Ô|Ồ|Ố|Ộ|Ổ|Ỗ|Ơ|Ờ|Ớ|Ợ|Ở|Ỡ)/", "O", $str);
        $str = preg_replace("/(Ù|Ú|Ụ|Ủ|Ũ|Ư|Ừ|Ứ|Ự|Ử|Ữ)/", "U", $str);
        $str = preg_replace("/(Ỳ|Ý|Ỵ|Ỷ|Ỹ)/", "Y", $str);
        $str = preg_replace("/(Đ)/", "D", $str);
        return $str;
    }

    public static function encodeLocation($str) {
        $str = strtolower(self::clearXSS($str));
        $str = preg_replace('/\s+/', '', $str);
        return $str;
    }
}
